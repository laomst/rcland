import { CClandBashGenerator } from './sections/ccland/bash'
import { CClandPowerShellGenerator } from './sections/ccland/powershell'

/** Fixed generation order */
const SECTION_ORDER = ['variables', 'path', 'functions', 'aliases', 'prompt', 'ccland'] as const

type SectionName = (typeof SECTION_ORDER)[number]

/** Registry: sectionName -> shellType -> generator */
const registry = new Map<string, Map<ShellType, SectionGenerator>>()

function reg(gen: SectionGenerator): void {
  if (!registry.has(gen.sectionName)) registry.set(gen.sectionName, new Map())
  registry.get(gen.sectionName)!.set(gen.shellType, gen)
}

// Register all section generators
reg(new VariablesZshGenerator())
reg(new VariablesBashGenerator())
reg(new VariablesPowerShellGenerator())
reg(new PathZshGenerator())
reg(new PathBashGenerator())
reg(new PathPowerShellGenerator())
reg(new FunctionsZshGenerator())
reg(new FunctionsBashGenerator())
reg(new FunctionsPowerShellGenerator())
reg(new AliasesZshGenerator())
reg(new AliasesBashGenerator())
reg(new AliasesPowerShellGenerator())
reg(new PromptZshGenerator())
reg(new PromptBashGenerator())
reg(new PromptPowerShellGenerator())
reg(new CClandZshGenerator())
reg(new CClandBashGenerator())
reg(new CClandPowerShellGenerator())

function getSection(name: string, shellType: ShellType): SectionGenerator | undefined {
  return registry.get(name)?.get(shellType)
}

/** Shebang / header by shell type */
function generateHeader(shellType: ShellType, timestamp: string): string {
  const shebangs: Record<ShellType, string> = {
    zsh: '#!/bin/zsh',
    bash: '#!/bin/bash',
    powershell: '# PowerShell'
  }
  return [
    shebangs[shellType],
    '# ============================================',
    '# CCland Shell Configuration',
    `# Generated: ${timestamp}`,
    `# Shell: ${shellType}`,
    '# WARNING: This file is auto-generated by CCland.',
    '# Do not edit manually - changes will be overwritten.',
    '# ============================================',
    ''
  ].join('\n')
}

/** Map section name to data from shell config / cc config */
function getSectionData(
  name: SectionName,
  shellConfig: ShellConfigData,
  cclandData: CClandSectionData
): unknown {
  switch (name) {
    case 'variables': return shellConfig.variables
    case 'path': return shellConfig.pathEntries
    case 'functions': return shellConfig.functions
    case 'aliases': return shellConfig.aliases
    case 'prompt': return shellConfig.prompt
    case 'ccland': return cclandData
  }
}

/**
 * Generate a complete shell configuration file.
 * Assembles all sections in fixed order: header -> variables -> path -> functions -> aliases -> prompt -> ccland.
 */
export function generateFullConfig(
  shellType: ShellType,
  shellConfig: ShellConfigData,
  ccConfig: CCLaunchData,
  decryptedTokens: Map<string, string>,
  ctx: GenerateContext
): string {
  const cclandData: CClandSectionData = { ccConfig, decryptedTokens }
  const parts: string[] = [generateHeader(shellType, ctx.timestamp)]

  for (const sectionName of SECTION_ORDER) {
    const gen = getSection(sectionName, shellType)
    if (!gen) continue

    const data = getSectionData(sectionName, shellConfig, cclandData)
    const output = gen.generate(data, ctx)
    if (output) {
      parts.push(output)
    }
  }

  parts.push('') // trailing newline
  return parts.join('\n\n')
}
