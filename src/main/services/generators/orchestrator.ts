// Section generators
import { VariablesZshGenerator } from './sections/variables/zsh'
import { VariablesBashGenerator } from './sections/variables/bash'
import { VariablesPowerShellGenerator } from './sections/variables/powershell'
import { PathZshGenerator } from './sections/path/zsh'
import { PathBashGenerator } from './sections/path/bash'
import { PathPowerShellGenerator } from './sections/path/powershell'
import { FunctionsZshGenerator } from './sections/functions/zsh'
import { FunctionsBashGenerator } from './sections/functions/bash'
import { FunctionsPowerShellGenerator } from './sections/functions/powershell'
import { AliasesZshGenerator } from './sections/aliases/zsh'
import { AliasesBashGenerator } from './sections/aliases/bash'
import { AliasesPowerShellGenerator } from './sections/aliases/powershell'
import { PromptZshGenerator } from './sections/prompt/zsh'
import { PromptBashGenerator } from './sections/prompt/bash'
import { PromptPowerShellGenerator } from './sections/prompt/powershell'
